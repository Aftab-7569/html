// arithmetic operators:
// + (addition)
// - (subtraction)
// * (multiplication)
// / (division)
// % (modulus, returns the remainder)
// ++ (increment by 1)
// -- (decrement by 1)

y = 555
z = 97

console.log(y+z);
console.log(y-z);
console.log(y*z);
console.log(y/z);
console.log(y%z);
console.log(++y);
console.log(--z);

// comparison operators:

// == (equal to)
// != (not equal to)
// === (strict equal to, checks value and type)
// !== (strict not equal to, checks value and type)
// > (greater than)
// < (less than)
// >= (greater than or equal to)
// <= (less than or equal to)

x=44
y=89

console.log(x==y);
console.log(x!=y);
console.log(x===y);
console.log(x!==y);
console.log(x>y);
console.log(x<y);
console.log(x>=89);
console.log(x<=89);

console.log(89 !== '89');

// logical operators:

// && (logical AND)
console.log((x<y) && (x==315));
// || (logical OR)
console.log((x<y) || (x==315));
// ! (logical NOT)
console.log(!(x<y));
